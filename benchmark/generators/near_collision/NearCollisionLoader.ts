import { createHash } from 'node:crypto';
import { readFileSync } from 'node:fs';
import { join } from 'node:path';

import type { CortexEvent, FamilyLoader, LoadCorpusOptions } from '../types.js';
import { LoaderError } from '../types.js';
import { computeCorpusRoot } from '../corpus_root.js';

// ── Constants ──────────────────────────────────────────────────────────────────

/**
 * SHA-256 of the canonical fixture file (JSON excluding corpus_hash field).
 * Placeholder until fixtures are generated by scripts/fetch-fixtures.mjs.
 */
const PINNED_CORPUS_HASH = 'FIXTURE_HASH_PLACEHOLDER';

const FIXTURE_PATH_SEGMENTS = [
  'benchmark', 'fixtures', 'near_collision', 'limit_nq_hotpotqa.json',
];

// Perturbation operators per specs/research_brief.md §4:
// bit-flip distance d ∈ {1, 2, 4} on derived binary keys.
export const BIT_FLIP_DISTANCES = [1, 2, 4] as const;

// ── Raw fixture types ──────────────────────────────────────────────────────────

interface RawNearCollisionItem {
  id: string;
  source: 'LIMIT' | 'BEIR_NQ' | 'BEIR_HotpotQA';
  query: string;
  passage: string;
  relevant: boolean;
  bit_flip_distance: number | null;
  protected: boolean;
  epoch_committed: number;
  source_ref: string;
  payload_hex: string;
}

interface FixtureFile {
  schema_version: string;
  sources: ('LIMIT' | 'BEIR_NQ' | 'BEIR_HotpotQA')[];
  license_spdx: string;
  corpus_hash: string;
  generated_at: string;
  items: RawNearCollisionItem[];
}

// ── Loader ─────────────────────────────────────────────────────────────────────

export class NearCollisionLoader implements FamilyLoader {
  static readonly LIMIT_PINNED_HASH = '90c5dd3a7c3c0d68bc6cc7a0c5b0f4ca124dfdcb';
  static readonly BEIR_PINNED_HASH = 'ef83d29307061c65d04b035b4f4e7c18bd8374af';

  /** MSMARCO and TREC-COVID: deferred pending commercial-use review. */
  static readonly DEFERRED_SUBSETS = ['MSMARCO', 'TREC_COVID'] as const;

  private readonly repoRoot: string;

  constructor(repoRoot?: string) {
    this.repoRoot = repoRoot ?? process.cwd();
  }

  async loadCorpus(epoch: number, opts?: LoadCorpusOptions): Promise<CortexEvent[]> {
    const fixturePath = join(this.repoRoot, ...FIXTURE_PATH_SEGMENTS);

    let raw: FixtureFile;
    try {
      const text = readFileSync(fixturePath, 'utf8');
      raw = JSON.parse(text) as FixtureFile;
    } catch (err) {
      throw new LoaderError(
        'SOURCE_NOT_FOUND',
        `NearCollisionLoader: fixture not found at ${fixturePath}. ` +
          'Run scripts/fetch-fixtures.mjs to generate it.\n' + String(err),
      );
    }

    validateFixtureHash(raw, fixturePath);

    let events = raw.items.map(rawToEvent);

    const epochFilter = opts?.epoch ?? epoch;
    events = events.filter((e) => e.epochCommitted <= epochFilter);

    if (opts?.protectedOnly) {
      events = events.filter((e) => e.isProtected);
    }

    if (opts?.limit !== undefined) {
      events = events.slice(0, opts.limit);
    }

    return events;
  }

  computeRoot(events: CortexEvent[]): Uint8Array {
    return computeCorpusRoot(events);
  }
}

// ── Internal helpers ───────────────────────────────────────────────────────────

function validateFixtureHash(raw: FixtureFile, fixturePath: string): void {
  if (PINNED_CORPUS_HASH === 'FIXTURE_HASH_PLACEHOLDER') return;

  const { corpus_hash: _h, ...rest } = raw;
  const canonical = JSON.stringify(rest, null, 2);
  const computed = createHash('sha256').update(canonical, 'utf8').digest('hex');

  if (computed !== PINNED_CORPUS_HASH) {
    throw new LoaderError(
      'CORPUS_HASH_MISMATCH',
      `NearCollisionLoader: CORPUS_HASH_MISMATCH\n` +
        `  expected: ${PINNED_CORPUS_HASH}\n` +
        `  computed: ${computed}\n` +
        `  fixture:  ${fixturePath}`,
    );
  }
}

function rawToEvent(item: RawNearCollisionItem): CortexEvent {
  return {
    id: item.id,
    family: 'near_collision',
    taskType: item.source,
    isProtected: item.protected,
    sourceRef: item.source_ref,
    payload: hexToBytes(item.payload_hex),
    queryText: item.query,
    truthText: item.passage,
    isStaleTruth: undefined,
    bitFlipDistance: item.bit_flip_distance ?? undefined,
    epochCommitted: item.epoch_committed,
  };
}

function hexToBytes(hex: string): Uint8Array {
  const s = hex.startsWith('0x') ? hex.slice(2) : hex;
  if (s.length % 2 !== 0) throw new RangeError(`hexToBytes: odd-length hex`);
  const out = new Uint8Array(s.length / 2);
  for (let i = 0; i < out.length; i++) {
    out[i] = parseInt(s.slice(i * 2, i * 2 + 2), 16);
  }
  return out;
}
